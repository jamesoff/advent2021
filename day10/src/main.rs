use std::ops::Index;

#[derive(PartialEq, Debug)]
enum Bracket {
    Round,
    Square,
    Curly,
    Pointy,
}

enum Element {
    Open(Bracket),
    Close(Bracket),
}

#[derive(PartialEq, Debug)]
enum LineResult {
    Corrupt(i32),
    Incomplete(Vec<Bracket>),
    Complete,
}

fn main() {
    println!("Hello, world!");
    println!("challenge1: {}", challenge1());
    println!("challenge2: {}", challenge2());
}

fn get_score(element: Bracket) -> i32 {
    use crate::Bracket::*;
    match element {
        Round => 3,
        Square => 57,
        Curly => 1197,
        Pointy => 25137,
    }
}

fn get_completion_score(mut brackets: Vec<Bracket>) -> i64 {
    use Bracket::*;
    let mut score = 0;
    while let Some(item) = brackets.pop() {
        score = score * 5 + match item {
            Round => 1,
            Square => 2,
            Curly => 3,
            Pointy => 4,
        }
    }
    score
}

fn parse_line(input: &str) -> LineResult {
    use crate::Bracket::*;
    use crate::Element::*;

    let mut stack = Vec::new();
    for char in input.chars() {
        let element: Element = match char {
            '<' => Open(Pointy),
            '>' => Close(Pointy),
            '(' => Open(Round),
            ')' => Close(Round),
            '{' => Open(Curly),
            '}' => Close(Curly),
            '[' => Open(Square),
            ']' => Close(Square),
            _ => panic!("bad char {}", char),
        };
        match element {
            Open(e) => stack.push(e),
            Close(e) => {
                if let Some(expecting) = stack.pop() {
                    if expecting != e {
                        println!("mismatched pop, got {:?} and expecting {:?}", e, expecting);
                        return LineResult::Corrupt(get_score(e));
                    }
                } else {
                    panic!("overpopped");
                }
            }
        }
    }
    if stack.len() > 0 {
        println!("remaining stack: {:?}", stack);
        return LineResult::Incomplete(stack);
    }
    LineResult::Complete
}

#[cfg(test)]
mod test {
    use super::*;

    #[test]
    fn test_parse_line() {
        use LineResult::Corrupt;
        assert_eq!(parse_line("{([(<{}[<>[]}>{[]{[(<()>"), Corrupt(1197));
        assert_eq!(parse_line("[[<[([]))<([[{}[[()]]]"), Corrupt(3));
        assert_eq!(parse_line("[{[{({}]{}}([{[{{{}}([]"), Corrupt(57));
        assert_eq!(parse_line("[<(<(<(<{}))><([]([]()"), Corrupt(3));
        assert_eq!(parse_line("<{([([[(<>()){}]>(<<{{"), Corrupt(25137));
    }

    #[test]
    fn test_2() {
        use Bracket::*;
        use LineResult::Incomplete;
        assert_eq!(
            parse_line("[({(<(())[]>[[{[]{<()<>>"),
            Incomplete(vec![
                Square, Round, Curly, Round, Square, Square, Curly, Curly
            ])
        );
        if let Incomplete(result) = parse_line("[({(<(())[]>[[{[]{<()<>>") {
            assert_eq!(get_completion_score(result), 288957);
        }
        else {
            panic!("bad test result");
        }
    }
}

